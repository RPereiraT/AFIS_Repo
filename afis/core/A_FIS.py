def _is_gaussian(x):
    """Check if x is a Gaussian membership function."""
    return isinstance(x, Gaussian)


def _has_gaussian_input(input_list):
    """Check if any dimension of the input is Gaussian."""
    for inp in input_list:
        if _is_gaussian(inp):
            return True
    return False


def _get_input_core(inp):
    """
    Extract core bounds from an input (for D_LR calculation).
    
    For Gaussian: returns (center, center) - point core
    For [a,b,c,d]: returns (b, c) - interval core
    """
    if _is_gaussian(inp):
        return [inp.center, inp.center]
    else:
        # [a, b, c, d] format
        return [inp[1], inp[2]]


def format_FN_N_Dim(fuzzy_number_n_dim, ftype='crisp_input'):
    ''' Given a fuzzy number, it changes its format to the admissible format supported by A_vee_B script suported types. 
    For now, two ftypes of inputs are supported
    - n dimensional crisp input (list)
    - n dimensional fuzzy antecedent (generated by Renato Lopes Moura wang mendel algorithm)
    - 1d consequent (generated by Renato Lopes Moura wang mendel algorithm)
    - Gaussian inputs are passed through unchanged (handled specially in A_FIS)
    
    Examples
    - 2D crisp input: [a,b] to [[a,a,a,a], [b,b,b,b]] 
    - 1D rule antecedent: see code below. '''

    FN_Ndim_adapted = []
    if ftype == 'crisp_input':  # Intended for crisp inputs (typical case)
        for comp_input in fuzzy_number_n_dim:
            # If Gaussian, pass through unchanged
            if _is_gaussian(comp_input):
                FN_Ndim_adapted.append(comp_input)
            else:
                FN_Ndim_adapted.append([comp_input] * 4)

    elif ftype == 'rule_antecedent':  # Intended for the case where fuzzy_number_n_dim correspond to the n dimensional antecedent of a rule
        for antecedent in fuzzy_number_n_dim:
            # If Gaussian, pass through unchanged
            if _is_gaussian(antecedent.set):
                FN_Ndim_adapted.append(antecedent.set)
                continue
            
            fset_type = str(type(antecedent.set))
            if fset_type.find('InferiorBorder') != -1:
                comp = [antecedent.set.top, antecedent.set.top, antecedent.set.top, antecedent.set.end]
            elif fset_type.find('SuperiorBorder') != -1:
                comp = [antecedent.set.ini, antecedent.set.top, antecedent.set.top, antecedent.set.top]
            elif fset_type.find('Triangular') != -1:
                comp = [antecedent.set.ini, antecedent.set.top, antecedent.set.top, antecedent.set.end]
            elif fset_type.find('Trapezoidal') != -1:
                comp = [antecedent.set.ini, antecedent.set.top1, antecedent.set.top2, antecedent.set.end]
            else:
                raise ValueError(f"Unsupported membership function type: {fset_type}")
            FN_Ndim_adapted.append(comp)

    elif ftype == 'rule_consequent':  # Intended for the case where fuzzy_number_n_dim correspond to the consequent of a rule
        cons = fuzzy_number_n_dim
        fset_type = str(type(cons.set))
        if fset_type.find('InferiorBorder') != -1:
            comp = [cons.set.top, cons.set.top, cons.set.top, cons.set.end]
        if fset_type.find('SuperiorBorder') != -1:
            comp = [cons.set.ini, cons.set.top, cons.set.top, cons.set.top]
        if fset_type.find('Triangular') != -1:
            comp = [cons.set.ini, cons.set.top, cons.set.top, cons.set.end]
        if fset_type.find('Trapezoidal') != -1:
            comp = [cons.set.ini, cons.set.top1, cons.set.top2, cons.set.end]
        FN_Ndim_adapted = comp
    
    return FN_Ndim_adapted


def fuzzy_imp(a, b, imp_type='luka', param=1):
    ''' Implements fuzzy implications I(a,b). a and b can be arrays of the same size'''
    
    a = np.asarray(a)
    b = np.asarray(b)

    if imp_type == 'luka':
        imp_a_b = np.minimum(1, 1 - a + b)
    elif imp_type == 'godel':
        imp_a_b = np.where(a <= b, 1, b)
    elif imp_type == 'goguen':
        imp_a_b = np.where(a <= b, 1, b / a)
    elif imp_type == 'hamacher':
        imp_a_b = np.where(a <= b, 1, b * (param + a - param * a) / (b * (param + a - param * a) + a - b))
    elif imp_type == 'dombi':
        # Handle edge cases to avoid division by zero
        # When a=0: implication should be 1 (anything implies from false)
        # When b=0 and a>0: implication should be 0
        # When a=1 and b<1: standard formula
        eps = 1e-10
        a_safe = np.clip(a, eps, 1 - eps)
        b_safe = np.clip(b, eps, 1 - eps)
        
        ratio_b = (1 - b_safe) / b_safe
        ratio_a = (1 - a_safe) / a_safe
        
        # Avoid negative base for fractional exponent
        diff = np.maximum(ratio_b ** param - ratio_a ** param, 0)
        imp_a_b = np.where(a <= b, 1, 1 / (1 + diff ** (1 / param)))

    return imp_a_b


def tnorms(a_constant, b_array, tnorm_type='product'):
    '''Implements a t-norm. a_constant is a single number. b_array is an array'''

    if isinstance(tnorm_type, str):
        if tnorm_type == 'product':
            out = a_constant * b_array
        elif tnorm_type == 'min':
            out = np.minimum(a_constant, b_array)
        elif tnorm_type == 'luka':
            aux = a_constant + b_array - 1
            out = np.maximum(0, aux)
        else:
            # Unknown string t-norm, fall back to product
            out = a_constant * b_array
    else:
        param = tnorm_type[1]
        if tnorm_type[0] == 'hamacher':
            num = a_constant * b_array
            den = param + (1 - param) * (a_constant + b_array - a_constant * b_array)
            # Avoid division by zero
            den = np.where(den == 0, 1e-10, den)
            out = num / den
        else:
            # Unknown parametric t-norm, fall back to product
            out = a_constant * b_array
    
    return out


def aggregation(x, agg_method='avg', param=[1]):
    ''' aggregation function'''

    if isinstance(agg_method, str):
        if agg_method == 'avg':
            agg_output = np.mean(x)
        elif agg_method == 'product':
            agg_output = np.prod(x)
        elif agg_method == 'min':
            agg_output = np.min(x)
        elif agg_method == 'max':
            agg_output = np.max(x)
        else:
            # Unknown string method, fall back to average
            agg_output = np.mean(x)
    else: 
        if agg_method[0] == 'weighted_avg':
            param = agg_method[1]
            # If weights are empty or sum to zero, fall back to simple average
            if len(param) == 0 or np.sum(param) == 0:
                agg_output = np.mean(x)
            else:
                agg_output = np.sum(np.array(x) * np.array(param)) / np.sum(param)
        else:
            # Unknown list method, fall back to average
            agg_output = np.mean(x)
    
    return agg_output


def membership(FN, U, disc=100):
    ''' calculates membership function of a fuzzy number [a,b,c,d]'''
    X = np.linspace(U[0], U[1], disc)
    FN_U = []
    for x in X:
        if (FN[1] <= x) and (x <= FN[2]):
            FN_U.append(1)
        elif (FN[0] < FN[1]) and (FN[0] <= x) and (x <= FN[1]):
            FN_U.append((x - FN[0]) / (FN[1] - FN[0]))
        elif (FN[2] < FN[3]) and (FN[2] <= x) and (x <= FN[3]):
            FN_U.append((x - FN[3]) / (FN[2] - FN[3]))
        else:
            FN_U.append(0)
    
    FN_U = np.array(FN_U)
    return FN_U


def antecedents_names(input_string):
    result = []
    length = len(input_string)

    for i in range(2, length):
        if input_string[i:i+2] == ' =':
            result.append(input_string[i-2:i])

    return result


def get_non_repeated_strings(list_of_lists, j):
    unique_strings = set()

    for lst in list_of_lists:
        unique_strings.add(lst[j])

    return list(unique_strings)


def cuenta_antecedentes(rule_base):
    Ant_names_list = []
    for rule in rule_base.ruleBase:
        input_string = str(rule.antecedents)
        Ant_names_list.append(antecedents_names(input_string))

    Antecedentes_list = []
    for j in range(len(Ant_names_list[0])):
        antecedente_j = get_non_repeated_strings(Ant_names_list, j)
        Antecedentes_list.append(sorted(antecedente_j))

    ANT_positions = []
    for ant in Ant_names_list:
        # Initialize an empty list to store the positions
        positions_list = []
        # Iterate over each element in ant and find its position in the corresponding list
        for element, sublist in zip(ant, Antecedentes_list):
            position = sublist.index(element)
            positions_list.append(position)
        ANT_positions.append(positions_list)

    return Antecedentes_list, Ant_names_list, ANT_positions


def interseccion_intervalos(INT_1, INT_2):
    ''' Determines if there's intersection between intervals. INTs are list of lists. The intersection is carried out
    between corresponding elements of both lists. The output is a boolean '''
    
    # Check for intersection for each pair of intervals
    intersect_array = [not (interval1[0] > interval2[1] or interval2[0] > interval1[1]) for interval1, interval2 in zip(INT_1, INT_2)]

    return intersect_array


